     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $products = new products;
        $products->productsName = $request->productsName;
        $products->productsLine= $request->productsLine;
        $products->productsScale = $request->productsScale;
        $products->productsVendor = $request->productsVendor;
        $products->productsDescription = $request->productsDescription;
        $products->quantityInStock = $request->quantityInStock;
        $products->buyPrice = $request->buyPrice;
        $products->MSRP = $request->MSRP;
        $products->save();
    }

